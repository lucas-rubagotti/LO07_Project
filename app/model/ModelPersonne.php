        $this->prenom = $prenom;
    }

    function setStatus($status) {
        $this->status = $status;
    }
    function setLogin($login) {
        $this->login = $login;
    }
    function setPassword($password) {
        $this->password = $password;
    }

    function getId() {
        return $this->id;
    }

    function getNom() {
        return $this->nom;
